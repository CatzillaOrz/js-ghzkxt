/**
 * @description Imperative vs Declarative
 * #Imerative code is that tells the machine what to do and how to do it
 * #Declarative tells machine what to do and what should happen
 *
 */

// #Imperative example:
for (let i = 0; i < 100; i++) {
    console.log(i);
}
// #Declarative example:
[1, 2, 3].forEach((item) => console.log(item));
